from Bio import SeqIO, PDB
import py3Dmol
import matplotlib.pyplot as plt
import pandas as pd

"""## ğŸ“¥ Upload a FASTA File (DNA or Protein)
You can upload a `.fasta` file (DNA or protein) and view its content.

"""

from google.colab import files
uploaded = files.upload()

fasta_file = list(uploaded.keys())[0]

record = SeqIO.read(fasta_file, "fasta")
sequence = str(record.seq)

print(f"ğŸ§¾ Sequence ID: {record.id}")
print(f"ğŸ§¬ Sequence Length: {len(sequence)}")
print(f"ğŸ§ª First 100 Bases/Residues:\n{sequence[:100]}...")

"""## ğŸ“Š Sequence Composition
We visualize the base (for DNA) or amino acid (for protein) composition.

"""

from collections import Counter

counts = Counter(sequence)
labels, values = zip(*sorted(counts.items()))

plt.figure(figsize=(10, 4))
plt.bar(labels, values, color='skyblue')
plt.title("Base / Residue Composition")
plt.xlabel("Symbol")
plt.ylabel("Count")
plt.grid(True, axis='y')
plt.show()

"""## ğŸ§¬ Upload a PDB File
Let's upload a `.pdb` structure file and render it using Py3Dmol.

"""

uploaded = files.upload()
pdb_file = list(uploaded.keys())[0]

# Show file name
print(f"ğŸ“ File: {pdb_file}")

# Read basic structure summary
parser = PDB.PDBParser()
structure = parser.get_structure("protein", pdb_file)

print("ğŸ“Š PDB Structure Summary:")
for model in structure:
    for chain in model:
        print(f"â›“ï¸ Chain: {chain.id}, Residues: {len(list(chain))}")

"""##ğŸ“¦ Code Cell â€“ 3D Visualization of PDB (Optional)"""

with open(pdb_file, 'r') as file:
    pdb_data = file.read()

view = py3Dmol.view(width=600, height=400)
view.addModel(pdb_data, 'pdb')
view.setStyle({'cartoon': {'color': 'spectrum'}})
view.zoomTo()
view.show()

